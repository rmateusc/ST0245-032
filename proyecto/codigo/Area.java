public class Area
{
    private int depth;
    private int width;
    private int height;
    public Area (int depth, int width, int height) {
        this.depth=depth;
        this.width=width;
        this.height=height;
    }
    
    public int getDepth() {
        return depth;
    }
    
    public int getWidth() {
        return width;
    }
    
    public int getHeight() {
        return height;
    }
}
